import { serve } from "https://deno.land/std@0.190.0/http/server.ts";
import Stripe from "https://esm.sh/stripe@18.5.0";
import { createClient } from "https://esm.sh/@supabase/supabase-js@2.57.2";

const corsHeaders = {
  "Access-Control-Allow-Origin": "*",
  "Access-Control-Allow-Headers": "authorization, x-client-info, apikey, content-type",
};

// Credit packages with price IDs
const CREDIT_PACKAGES: Record<string, { priceId: string; words: number; amount: number }> = {
  "25000": {
    priceId: "price_1SsNyUBqXALGTPIrMfVes3Ca",
    words: 25000,
    amount: 1990,
  },
  "50000": {
    priceId: "price_1SsNyVBqXALGTPIrhX4ZinrH",
    words: 50000,
    amount: 3490,
  },
  "100000": {
    priceId: "price_1SsNyWBqXALGTPIrRZDAJytx",
    words: 100000,
    amount: 5990,
  },
};

serve(async (req) => {
  if (req.method === "OPTIONS") {
    return new Response(null, { headers: corsHeaders });
  }

  const supabaseClient = createClient(
    Deno.env.get("SUPABASE_URL") ?? "",
    Deno.env.get("SUPABASE_ANON_KEY") ?? ""
  );

  try {
    // Authenticate user
    const authHeader = req.headers.get("Authorization")!;
    const token = authHeader.replace("Bearer ", "");
    const { data } = await supabaseClient.auth.getUser(token);
    const user = data.user;
    
    if (!user?.email) {
      throw new Error("User not authenticated or email not available");
    }

    const { packageId } = await req.json();
    const creditPackage = CREDIT_PACKAGES[packageId as keyof typeof CREDIT_PACKAGES];
    
    if (!creditPackage) {
      throw new Error("Invalid credit package");
    }

    // Initialize Stripe
    const stripe = new Stripe(Deno.env.get("STRIPE_SECRET_KEY") || "", {
      apiVersion: "2025-08-27.basil",
    });

    // Check if customer exists
    const customers = await stripe.customers.list({ email: user.email, limit: 1 });
    let customerId: string | undefined;
    if (customers.data.length > 0) {
      customerId = customers.data[0].id;
    }

    // Create checkout session
    const session = await stripe.checkout.sessions.create({
      customer: customerId,
      customer_email: customerId ? undefined : user.email,
      line_items: [
        {
          price: creditPackage.priceId,
          quantity: 1,
        },
      ],
      mode: "payment",
      success_url: `${req.headers.get("origin")}/dashboard?credit_purchase=success`,
      cancel_url: `${req.headers.get("origin")}/dashboard?credit_purchase=cancelled`,
      metadata: {
        user_id: user.id,
        words_purchased: creditPackage.words.toString(),
        package_id: packageId,
      },
    });

    // Create pending purchase record
    const supabaseAdmin = createClient(
      Deno.env.get("SUPABASE_URL") ?? "",
      Deno.env.get("SUPABASE_SERVICE_ROLE_KEY") ?? ""
    );

    await supabaseAdmin.from("credit_purchases").insert({
      user_id: user.id,
      stripe_session_id: session.id,
      amount: creditPackage.amount,
      words_purchased: creditPackage.words,
      status: "pending",
    });

    return new Response(
      JSON.stringify({ url: session.url }),
      {
        headers: { ...corsHeaders, "Content-Type": "application/json" },
        status: 200,
      }
    );
  } catch (error) {
    console.error("Error creating credit purchase:", error);
    const errorMessage = error instanceof Error ? error.message : "Unknown error";
    return new Response(
      JSON.stringify({ error: errorMessage }),
      {
        headers: { ...corsHeaders, "Content-Type": "application/json" },
        status: 500,
      }
    );
  }
});
